// Take two numbers as input and display their sum, difference, product, and division result.
#include <stdio.h>
int main () {
    float a, b;
    printf("Enter number a= ");
    scanf("%f", &a);
    printf("Enter number b= ");
    scanf("%f", &b);
    printf("Sum: %f \n", a+b);
    printf("Difference: %f \n", a-b);
    printf("Product: %f \n", a*b);
    printf("Division: %f \n", a/b);
     return 0;



}